package com.company.Q5;

public class Q5Student{
    String name;
    Double age;
    Double score;

    public Q5Student(String name, Double age, Double score) {
        this.name = name;
        this.age = age;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public Double getAge() {
        return age;
    }

    public Double getScore() {
        return score;
    }
}
