package com.company.Q4;

public class Q4Employee {
    String name;
    Double salary;
    Double age;

    public Q4Employee(String name, Double salary, Double age) {
        this.name = name;
        this.salary = salary;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public Double getSalary() {
        return salary;
    }

    public Double getAge() {
        return age;
    }
}
